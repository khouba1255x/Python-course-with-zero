print(100 != 100.00)

print("=" * 40)
# Greater Than + Less Than :
print("# Greater Than + Less Than :")

print(100 > 100)
print(100 > 200)
print(100 > 100.00)
print(100 > 40)

print("=" * 10)

print(100 < 100)
print(100 < 200)
print(100 < 100.00)
print(100 < 40)

