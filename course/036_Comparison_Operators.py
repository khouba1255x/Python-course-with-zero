# --------------------------
# -- Comparison Operators --
# --------------------------
# [ == ] Equal
# [ != ] Not Equal
# [ > ] Greater Than
# [ < ] Less Than
# [ >= ] Greater Than Or Equal
# [ <= ] Less Than Or Equal
# --------------------------

print("=" * 40)
# Equal + Not Equal :
print("# Equal + Not Equal :")

print(100 == 100)
print(100 == 200)
print(100 == 100.00)

print("=" * 10)

print(100 != 100)
