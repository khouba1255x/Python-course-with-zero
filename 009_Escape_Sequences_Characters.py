# Back Space
print("Hello\bWorld")

# Escape New Line + \
print("Hello\
      I love\
      Python")

# Escape Back Slash
print("I Love Back Slash \\")

# Escape Single Quote
print('I Love Single Quote \'test\' ')

# Escape Double Quaote
print("I Love Double Quaotes \"Test\" ")

# Line Feed
print("Hello World\nSecond Line")

# Carriage Return
print("123456\rAbcde")

# Horizontal Tab
print("Hello\tPython")

# Character Hex Value
print("\x4F\x73")
